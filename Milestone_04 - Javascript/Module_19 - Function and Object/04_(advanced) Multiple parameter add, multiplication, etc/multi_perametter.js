function add(n1, n2) {
    var total = n1 + n2;
    return total;
}

var sum = add(23, 45);
console.log(sum);

// Substructions
function sub(n1, n2) {
    var total = n1 - n2;
    return total
}

var substraction = sub(93, 67);
console.log(substraction);

// Multipication
function multi(n1, n2) {
    var total = n1 * n2;
    return total
}

var multipication = multi(5, 6);
console.log(multipication);

// Division
function div(n1, n2) {
    var total = n1 / n2;
    return total;
}

var division = div(23, 4);
